package session_4_java_operators_and_loops.practice.challenge;

public class Challenge13 {
    public static void main(String[] args) {

        boolean isRaining = true;

        boolean isSunny = false;

        boolean notRaining = !isRaining;

        boolean notSunny = !isSunny;

        System.out.println("The value of isRaining is: " + isRaining);
        System.out.println("The value of notRaining is: " + notRaining);
        System.out.println("The value of isSunny is " + isSunny);
        System.out.println("The value of notSunny is "+ notSunny);
    }
}
