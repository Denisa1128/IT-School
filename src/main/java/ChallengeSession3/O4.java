package ChallengeSession3;

public class O4 {
    public static void main(String[] args) {
        int counter = 1;
        while (counter < 10) {
            counter = ++counter;
            System.out.println(counter);
        }

        while (counter  >0) {
            counter = --counter;
            System.out.println(counter);
        }


    }
}
